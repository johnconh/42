char	*ft_strcat(char *dest, char *src)
{
	int	d;
	int	c;

	d = 0;
	while (dest[d])
	{
		d++;
	}
	c = 0;
	while (src[c])
	{
		dest[d] = src[c];
		c++;
		d++;
	}
	dest[d] = '\0';
	return (dest);
}
